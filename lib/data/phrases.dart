  /// Banco PÚBLICO: semente + expansão ([PhraseExtra]), montado uma vez.
  ///
  /// Mescla frases novas nas categorias existentes (sem duplicar pelo texto) e
  /// acrescenta as categorias novas ao fim. O conteúdo remoto continua sendo
  /// mesclado por cima disto pelo `ContentRepository`.
  static final List<PhraseCategory> bundled = _build();

  static List<PhraseCategory> _build() {
    final byId = {for (final c in _seed) c.id: c};
    final order = [for (final c in _seed) c.id];

    // 1) Expande categorias existentes com frases sem autor.
    PhraseExtra.morePhrases.forEach((id, extra) {
      final base = byId[id];
      if (base == null) return;
      byId[id] = base.merge(extra, List<String?>.filled(extra.length, null));
    });

    // 1b) Lotes bônus: mais frases (sem autor) para categorias existentes.
    for (final pack in [PhraseExtra.bonusPhrases, PhraseExtra.bonusPhrases2]) {
      pack.forEach((id, extra) {
        final base = byId[id];
        if (base == null) return;
        byId[id] = base.merge(extra, List<String?>.filled(extra.length, null));
      });
    }

    // 2) Expande categorias existentes que têm autor (frase + autor pareados).
    PhraseExtra.morePhrasesWithAuthor.forEach((id, extra) {
      final base = byId[id];
      if (base == null) return;
      final authors = PhraseExtra.moreAuthors[id] ??
          List<String?>.filled(extra.length, null);
      byId[id] = base.merge(extra, authors);
    });

    // 3) Acrescenta categorias inteiramente novas (+ lotes bônus + autores).
    for (final c in [
      ...PhraseExtra.newCategories,
      ...PhraseExtra.bonusCategories,
      ...PhraseExtra.bonusCategories2,
      ...PhraseExtra.authorCategories,
    ]) {
      final existing = byId[c.id];
      if (existing == null) {
        byId[c.id] = c;
        order.add(c.id);
      } else {
        byId[c.id] = existing.merge(c.phrases, c.authors ?? const <String?>[]);
      }
    }

    return [for (final id in order) byId[id]!];
  }

  // ---------- helpers da base embutida ----------
  static List<PhraseCategory> get categories => bundled;

  static PhraseCategory categoryById(String id) =>
      bundled.firstWhere((c) => c.id == id, orElse: () => bundled.first);

  static List<Phrase> get allPhrases => flatten(bundled);

  static List<Phrase> phrasesOf(String categoryId) =>
      flatten([categoryById(categoryId)]);

  /// Expande uma lista de categorias em frases planas (com autor, se houver).
  static List<Phrase> flatten(List<PhraseCategory> cats) {
    final out = <Phrase>[];
    for (final c in cats) {
      for (var i = 0; i < c.phrases.length; i++) {
        out.add(Phrase(
          text: c.phrases[i],
          categoryId: c.id,
          categoryName: c.name,
          gradient: c.gradient,
          author: c.authorAt(i),
        ));
      }
    }
    return out;
  }
